'Title: Using plant functional traits to predict ecosystem vulnerability to changing fire regimes
Date: 2013-08-01 00:00
Category: Publications
status: published
tag: Publications

Australia lacks a coherent approach for synthesizing plant functional traits to
predict the response of ecosystems to
changing fire regimes. Large plant community datasets already exist for major
ecosystems in Australia, from rainforests
to deserts. This working group aimed to attribute species into fire persistence
groups, with the objective of predicting
the persistence (bud resprouting type) of the species comprising a community in
relation to resource gradients and
fire regimes using a new scheme, the Buds-Protection-Resource scheme (BPR)
(Clarke et al. 2013). Our analyses
focussed on creating predictive models with accompanying levels of confidence.
Our working group added value to
the work of a previous ACEAS working group (Murphy et al. 2011; Bowman et al.
    2013; Murphy et al. 2013) by linking
their predicted changes in fire regimes to biodiversity effects. Our analyses
incorporate an understanding of plant fire
resilience traits and the role of the persistence niche into the whole of
continent approach developed previously.

